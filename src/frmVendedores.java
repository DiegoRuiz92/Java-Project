    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (!txtDocumentoVendedor.getText().isEmpty() && !txtNombreVendedor.getText().isEmpty() && !txtCorreoVendedor.getText().isEmpty()) {
            cc = new Conexion();
            cn = cc.conectar();

            try {
                PreparedStatement ps = cn.prepareStatement("INSERT INTO vendedores VALUES (?,?,?)");
                ps.setString(1, txtDocumentoVendedor.getText());
                ps.setString(2, txtNombreVendedor.getText());
                ps.setString(3, txtCorreoVendedor.getText());
                ps.executeUpdate();
                mostrarTodo();

            } catch (SQLException ex) {
                System.out.print(ex.getMessage());
            }

            limpiar();
        } else {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos para guardar un registro", "¡Atención!", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

