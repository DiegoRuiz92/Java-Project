    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int opcion = JOptionPane.showConfirmDialog(null, "Está seguro que desea eliminar el registro?");

        if (opcion == 0) {
            cc = new Conexion();
            cn = cc.conectar();

            try {
                PreparedStatement ps = cn.prepareStatement("DELETE FROM vendedores WHERE documento_vendedor = ?");
                ps.setString(1, txtDocumentoVendedor.getText());
                ps.executeUpdate();
                mostrarTodo();
            } catch (SQLException ex) {
                System.out.print(ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Canceló la operación");
        }


    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        int opcion = JOptionPane.showConfirmDialog(null, "Está seguro que desea actualizar este registro?");

        if (opcion == 0) {
            cc = new Conexion();
            cn = cc.conectar();

            try {
                PreparedStatement ps = cn.prepareStatement("UPDATE vendedores SET nombre = ?, correo = ? WHERE documento_vendedor = ?");
                ps.setString(1, txtNombreVendedor.getText());
                ps.setString(2, txtCorreoVendedor.getText());
                ps.setString(3, txtDocumentoVendedor.getText());
                ps.executeUpdate();
                mostrarTodo();

            } catch (SQLException ex) {
                System.out.print(ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Canceló la operación");
        }


    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            cc = new Conexion();
            cn = cc.conectar();
            ps = cn.prepareStatement("SELECT * FROM vendedores WHERE documento_vendedor = ?");
            ps.setString(1, JOptionPane.showInputDialog("Ingrese el documento a buscar"));
            rs = ps.executeQuery();

            if (rs.next()) {
                //txtDocumentoProfesor.setText(rs.getString("documento"));
                txtNombreVendedor.setText(rs.getString("nombre"));
                txtCorreoVendedor.setText(rs.getString("correo"));

            } else {
                JOptionPane.showMessageDialog(null, "No existe el vendedor en la base de datos");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

