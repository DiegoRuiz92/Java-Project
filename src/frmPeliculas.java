    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (!txtIdPelicula.getText().isEmpty() && !txtNombrePelicula.getText().isEmpty() && !txtPrecio.getText().isEmpty() && !cmbGenero.getSelectedItem().equals(0) && !cmbHora.getSelectedItem().equals(0)) {
            cc = new Conexion();
            cn = cc.conectar();

            try {
                PreparedStatement ps = cn.prepareStatement("INSERT INTO pelicula VALUES (?,?,?,?,?)");
                ps.setString(1, txtIdPelicula.getText());
                ps.setString(2, txtNombrePelicula.getText());
                ps.setString(3, (String) cmbGenero.getSelectedItem());
                ps.setString(4, (String) cmbHora.getSelectedItem());
                ps.setInt(5, Integer.parseInt(txtPrecio.getText()));
                ps.executeUpdate();
                mostrarTodo();
            } catch (SQLException ex) {
                System.out.print(ex.getMessage());
            }

            limpiar();
        } else {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos para guardar un registro", "¡Atención!", JOptionPane.WARNING_MESSAGE);

        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
        txtIdPelicula.setEnabled(true);
        txtNombrePelicula.setEnabled(true);
        cmbGenero.setEnabled(true);
        cmbHora.setEnabled(true);
        txtPrecio.setEnabled(true);

        fila = Tabla.getSelectedRow();
        txtIdPelicula.setText(String.valueOf(Tabla.getValueAt(fila, 0)));
        txtNombrePelicula.setText(String.valueOf(Tabla.getValueAt(fila, 1)));
        cmbGenero.setSelectedItem(Tabla.getValueAt(fila, 2));
        cmbHora.setSelectedItem(Tabla.getValueAt(fila, 3));
        txtPrecio.setText(String.valueOf(Tabla.getValueAt(fila, 4)));

    }//GEN-LAST:event_TablaMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int opcion = JOptionPane.showConfirmDialog(null, "Está seguro que desea eliminar el registro?");

        if (opcion == 0) {
            cc = new Conexion();
            cn = cc.conectar();

            try {
                PreparedStatement ps = cn.prepareStatement("DELETE FROM pelicula WHERE id_pelicula = ?");
                ps.setString(1, txtIdPelicula.getText());
                ps.executeUpdate();
                mostrarTodo();
            } catch (SQLException ex) {
                System.out.print(ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Canceló la operación");
        }


    }//GEN-LAST:event_btnEliminarActionPerformed

