    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            cc = new Conexion();
            cn = cc.conectar();
            ps = cn.prepareStatement("SELECT * FROM pelicula WHERE id_pelicula = ?");
            ps.setString(1, JOptionPane.showInputDialog("Ingrese el id de la película a buscar"));
            rs = ps.executeQuery();

            if (rs.next()) {
                //txtDocumentoProfesor.setText(rs.getString("documento"));
                txtNombrePelicula.setText(rs.getString("nombre"));
                cmbGenero.setSelectedItem(rs.getString("genero"));
                cmbHora.setSelectedItem(rs.getString("hora"));
                txtPrecio.setText(rs.getString("precio"));

            } else {
                JOptionPane.showMessageDialog(null, "No existe la película en la base de datos");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    public void limpiar() {

        txtIdPelicula.setText(" ");
        txtNombrePelicula.setText(" ");
        cmbGenero.setSelectedIndex(0);
        cmbHora.setSelectedIndex(0);
        txtPrecio.setText(" ");

        txtIdPelicula.requestFocus();
    }

    public void mostrarTodo() {
        modelo = new DefaultTableModel();

        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Género");
        modelo.addColumn("Hora");
        modelo.addColumn("Precio");

        try {
            cc = new Conexion();
            cn = cc.conectar();
            ps = cn.prepareStatement("SELECT * FROM pelicula");
            rs = ps.executeQuery();

            String[] dato = new String[5];
            while (rs.next()) {
                dato[0] = rs.getString(1);
                dato[1] = rs.getString(2);
                dato[2] = rs.getString(3);
                dato[3] = rs.getString(4);
                dato[4] = rs.getString(5);

                modelo.addRow(dato);
            }

            Tabla.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

